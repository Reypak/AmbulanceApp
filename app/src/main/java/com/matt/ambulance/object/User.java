package com.matt.ambulance.object;

public class User {
    String email;
    String phone;
    String uname;
    String hospital;
    String ambNo;
    int accountType;

    public User() {
    }

    public String getEmail() {
        return email;
    }

    public String getPhone() {
        return phone;
    }

    public String getUname() {
        return uname;
    }

    public String getHospital() {
        return hospital;
    }

    public String getAmbNo() {
        return ambNo;
    }

    public int getAccountType() {
        return accountType;
    }
}
